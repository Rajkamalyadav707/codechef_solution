import java.util.*;


class Codechef {
	public static void main (String[] args) throws java.lang.Exception {
	Scanner scan = new Scanner(System.in);
		
		while(scan.hasNextInt()) {
			
			int n = scan.nextInt();
			if(n==42) {
				break;
			}
			System.out.println(n);
        }
        scan.close();
    }
    

}
